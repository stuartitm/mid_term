CREATE SEQUENCE DELITOS_SEQ
  MINVALUE 100
  MAXVALUE 200
  START WITH 100
  INCREMENT BY 1;
  
CREATE SEQUENCE CAPTURAS_SEQ
  MINVALUE 1000
  MAXVALUE 2000
  START WITH 1000
  INCREMENT BY 1;
  
CREATE TABLE DELITOS(id int, nombre VARCHAR2(255));
CREATE TABLE CAPTURAS(ID int, FECHA timestamp, DEPARTAMENTO VARCHAR2(255), MUNICIPIO VARCHAR2(255), 
  DIA int, BARRIO VARCHAR2(255), ZONA VARCHAR2(255), CLASE_SITIO VARCHAR2(255), EDAD int, SEXO VARCHAR2(255), ESTADO_CIVIL VARCHAR2(255), 
  CLASE_EMPLEADO VARCHAR2(255), PROFESION VARCHAR2(255), ESCOLARIDAD VARCHAR2(255), DELITO_ID int foreign key references DELITOS(id));
  